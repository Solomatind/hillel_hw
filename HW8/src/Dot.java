
public class Dot {
    
    private int x;
    private int y;
    
    Dot() {
        x = 0;
        y = 0;
    }
    
    Dot(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
}
