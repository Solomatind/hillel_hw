package com.hillel.figure;

public class Dot {

    private double x;
    private double y;

    public Dot() {
        x = 0;
        y = 0;
    }

    public Dot(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
