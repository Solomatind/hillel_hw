
public class Car {

    int id;
    String brand;
    String model;
    int year;
    String color;
    double cost;
    String registerNumber;

    Car(int id, String brand, String model, int year, String color, double cost, 
        String registerNUmber) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.cost = cost;
        this.registerNumber = registerNUmber;
    }

}
